#import "MMNamedJobSearchRequest.h"

@implementation MMNamedJobSearchRequest

@synthesize searchName;
@synthesize searchParameters;

- (id) initWithName:(NSString *) sName forParameters:(NSDictionary *) searchParams {
    self = [super init];
    if (self) {
        self.searchName = sName;
        self.searchParameters = searchParams;
        
        return self;
    } else return nil;
}

@end
